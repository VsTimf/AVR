//set to upper left corner
void LCD_pos_0()
{
   	LCD_write_byte(0b00000000,1); // x = 0
   	LCD_write_byte(0b00100000,1); // y a0, a1,a2, a3 = 0
   	LCD_write_byte(0b00110000,1); // y a6,a5,a4
	lcd.x_pos = 0;
	lcd.y_pos = 0;
}

void LCD_y_pos(unsigned char y)
{ 
	unsigned char myy;
	
	if ((y<0)||(y>LCD_Y_MAX)) 
		return;
	
	lcd.y_pos = y;
  	myy = 0b00100000 + (0b00001111 & y); // low 4 bits
  	LCD_write_byte(myy, 1);

  	myy = 0b01110000 & y; // high 3 bits

	myy >>= 4;
  	myy = myy  + 0b00110000;
  	LCD_write_byte(myy, 1);
}

void LCD_x_pos(unsigned char x)
{
	if ((x<0)||(x>LCD_X_MAX)) 
		return;
	
	lcd.x_pos = x;
  	LCD_write_byte(0b00001111 & x,1);
}


void LCD_xy_pos(uint8_t x, uint8_t y)
{
	unsigned char myy;
	
	if ((x<0)||(x>LCD_X_MAX)) 
		return;
		
	if ((y<0)||(y>LCD_Y_MAX)) 
		return;
	
	lcd.x_pos = x;
	LCD_write_byte(0b00001111 & x,1);
	
	lcd.y_pos = y;
	myy = 0b00100000 + (0b00001111 & y); // low 4 bits
	LCD_write_byte(myy, 1);

	myy = 0b01110000 & y; // high 3 bits

	myy >>= 4;
	myy = myy  + 0b00110000;
	LCD_write_byte(myy, 1);
}


