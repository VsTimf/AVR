void LCD_write_data(unsigned char c)
{
	unsigned char __c2;
	
	LCDPORT &= ~(SCL);
	__c2 =c;

	LCDPORT &= ~(RS);// its data

	// transmit byte from highest bit to lowest bit
	for (unsigned char i=0; i<8; i++)
	{
		LCDPORT &= ~(SCL);
		if ( (__c2 & 128)==0 )
		LCDPORT &= ~(SDA);
		else
		LCDPORT |=(SDA);
		__c2=__c2<<1;
		_delay_us(WDELAY);
		LCDPORT |=(SCL);// when SCL goes high data on PIN SID are accepted by controller
		_delay_us(WDELAY);
	}
	LCDPORT &= ~(SCL); 	// prepare for next byte
	LCDPORT &= ~(SDA);
}




void LCD_put_digit(uint8_t dig)
{
	unsigned pos = 0;
	uint8_t _ypos = lcd.y_pos;

	if(dig > 9) return;
	
	pos = dig * Bitmap_h[2];
	
	LCD_write_byte(0b10100001,1); 	//increment control register; increment x+y=0011 increment x=0001; increment y = 0010				 Incerment control set
	

	for (uint8_t ypos=0; ypos < Bitmap_h[1]; ypos++)
	{
		for (uint8_t xpos=0; xpos < Bitmap_h[0]; xpos++)
			LCD_write_data(pgm_read_byte(&(Bitmaps[pos++])));
		
		LCD_x_pos(lcd.x_pos);
		LCD_y_pos(lcd.y_pos+1);
	}
		
	LCD_write_byte(0b10100010,1); 	//increment control register; increment x+y=0011 increment x=0001; increment y = 0010				 Incerment control set
	
	LCD_x_pos(lcd.x_pos + Bitmap_h[0]);
	LCD_y_pos(_ypos);
}





// data: data_command = 0
// command: data_command = 1
// clear_sreen: data_command = 3
void LCD_write_byte(unsigned char c, int data_command)
{
	unsigned char __c2;
	
  	LCDPORT &= ~(SCL);
	__c2 =c;

	if (data_command == 1)
		LCDPORT |=(RS);// its command 
	else
		LCDPORT &= ~(RS);// its data

	// transmit byte from highest bit to lowest bit
	for (unsigned char i=0; i<8; i++)
	{
		LCDPORT &= ~(SCL);
		if ( (__c2 & 128)==0 )
			LCDPORT &= ~(SDA);
		else
			LCDPORT |=(SDA);
		__c2=__c2<<1;
		_delay_us(WDELAY);
		LCDPORT |=(SCL);// when SCL goes high data on PIN SID are accepted by controller
		_delay_us(WDELAY);
	}	
	LCDPORT &= ~(SCL); 	// prepare for next byte
   	LCDPORT &= ~(SDA);
	   
	// set new x and y coordinates if DATA (not command nor clear screen
	if (data_command == 0)
	{ 
		if (lcd.y_pos<LCD_Y_MAX)
			lcd.y_pos++;
		else
			lcd.y_pos=0; // increment loop 
	}
}



