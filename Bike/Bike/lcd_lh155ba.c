void LCD_putc(char c)
{ 
	unsigned char i,ii, y;

	if (c=='\n') 			// line feed
	{ 
		LCD_x_pos(0); 
		
		if (lcd.y_pos+9 < LCD_Y_MAX)
			LCD_y_pos(lcd.y_pos + 8);
 		else
			LCD_y_pos (0);
		return; 
	}	

	if ((c<32)||(c>223)) return;
	ii = (c-32) % 32;
	y = lcd.y_pos;
	
	for (i=0; i<8; i++) 
	{
		switch ((c-32)/32)
		{
			case 0:
				LCD_write_byte(pgm_read_byte(&(_LCD1_1_FONT[ii][i])),0);
				break;
			
			case 1:
				LCD_write_byte(pgm_read_byte(&(_LCD1_2_FONT[ii][i])),0);
				break;
			
			case 2:
				LCD_write_byte(pgm_read_byte(&(_LCD1_3_FONT[ii][i])),0);
				break;
			
			case 3:
				LCD_write_byte(pgm_read_byte(&(_LCD1_4_FONT[ii][i])),0);
				break;
		}
	}
	
	if (lcd.x_pos<LCD_X_MAX)
	{
		LCD_y_pos(y);
		LCD_x_pos(lcd.x_pos+1);
	}
	else
	{
		if (lcd.y_pos==0) // right downmost position !
			LCD_pos_0();
		else // line feed
			LCD_x_pos(0);
	}
}

void LCD_printf(char* str)
{
	unsigned int i,Len = strlen(str);
	
	for(i = 0; i<Len; i++)
		LCD_putc(str[i]);
}


