void LCD_draw_image (unsigned char x,unsigned char y,char* Pimage)
{
	unsigned char Xpos = lcd.x_pos, Ypos = lcd.y_pos;
	unsigned char x1,y1;
	
	if (Pimage != NULL)
	{	
		char* Pcolumn = Pimage;
		for (x1 = 0; x1 < x; x1++)
		{
			for (y1 = 0; y1 < y; y1++)
			{
				LCD_write_byte(pgm_read_byte(Pcolumn + y1*x), 0);
				//LCD_write_byte(*(Pcolumn + y1*x), 0);
			}
			Pcolumn++;
			LCD_y_pos(Ypos);
			LCD_x_pos(++Xpos);
		}			
	}
	else
	{
		for (x1 = 0; x1 < x; x1++)
		{
			for (y1 = 0; y1 < y; y1++)
			{
				LCD_write_byte(0x00, 0);
			}
			LCD_y_pos(Ypos);
			LCD_x_pos(++Xpos);
		}
	}
}




