void LCD_clear_display()
{  
	long i;

	// pos = 0
  	LCD_write_byte(0b00000000,1); // x = 0
   	LCD_write_byte(0b00100000,1); // y a0, a1,a2, a3 = 0
   	LCD_write_byte(0b00110000,1); // y a6,a5,a4

	LCD_write_byte(0b10100011,1); //increment control register; increment x+y=0011
			     // increment x=0001
	for (i=0; i<(LCD_X_MAX+1)*(LCD_Y_MAX+1); i++)
		LCD_write_byte(0,3);
        
	LCD_write_byte(0b10100010,1); 	//increment control register; increment x+y=0011
			     		// increment y=0010
	// pos = 0
  	LCD_write_byte(0b00000000,1); // x = 0
   	LCD_write_byte(0b00100000,1); // y a0, a1,a2, a3 = 0
   	LCD_write_byte(0b00110000,1); // y a6,a5,a4
	   
	lcd.x_pos = 0;
	lcd.y_pos = 0;   
}



