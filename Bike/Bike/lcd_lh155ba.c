// Init Display, power it on etc.
void LCD_init()
{
	LCDPORT &= ~(RES);		// Reset
	LCDPORT &= ~(SCL);
	LCDPORT &= ~(SDA);		
	LCDPORT &= ~(RS);		// 0 - Data to RAM    1 - command
	
	_delay_ms(10); // wait for power to stabilize
	
	LCDPORT |=(RES);
	
	_delay_ms(1);
	
   	LCD_write_byte(0b10110001,1);  // power control register	Reset  
	   
   	_delay_ms(1);
	   
   	LCD_write_byte(0b10110010,1);  // power control register   Power ON
   	LCD_write_byte(0b10000001,1);  // display control register	Display ON
	   
   	_delay_ms(1);
	   
	// ***********************************************************************+
	// This is very important: set contrast
	// Thanks to J.Sturz and T.Winkler from TI
	
   	LCD_write_byte(0b11011100,1);	// contrast level VERY low 10 = 1010 contrast level high 13=1101
   	LCD_write_byte(0b10010011,1);		// Display swap lets X begin in upper left corner // default is upper left corner  SWAP, REF     Display control 2
  	LCD_write_byte(0b10100010,1); 	//increment control register; increment x+y=0011 increment x=0001; increment y = 0010				 Incerment control set
	
}

/* To reduce energy use functions below */
void LCD_ON()
{
   	LCD_write_byte(0b10110010,1);  // power control register   Power ON
   	LCD_write_byte(0b10000001,1);  // display control register	Display ON
}


void LCD_OFF()
{
	LCD_write_byte(0b10000000,1);  // display control register	Display OFF
	LCD_write_byte(0b10110000,1);  // power control register   Power OFF
}

